package t5;

public class Holder{

    public String toString(){
	return new String("" + 
			  "LOLSIZE Integer: " + o + "\n" + 
			  "LOLSIZE double: " + d + "\n" +
			  "LOLSIZE float: " + f + "\n" +
			  "LOLSIZE long: " + l + "\n" +
			  "LOLSIZE int: " + i + "\n" +
			  "LOLSIZE short: " + s + "\n" +
			  "LOLSIZE char: " + c + "\n" +
			  "LOLSIZE boolean: " + b + "\n" + 
			  "LOLSIZE byte: " + by);
    }

    private Integer o;
    private double d;
    private float f;
    private long l;
    private int i;
    private short s;
    private char c;
    private boolean b;
    private byte by;

    public Holder(Integer o,
		     double d,
		     float f,
		     long l,
		     int i,
		     short s,
		     char c,
		     boolean b,
		     byte by){
	this.o = o;
	this.d = d;
	this.f = f;
	this.l = l;
	this.i = i;
	this.s = s;
	this.c = c;
	this.b = b;
	this.by = by;
    }

    public Integer getO(){
	return this.o;
    }
    
    public void setO(Integer o){
	this.o = o;
    }

    public double getD(){
	return this.d;
    }
    
    public void setD(double d){
	this.d = d;
    }

    public float getF(){
	return this.f;
    }
    
    public void setF(float f){
	this.f = f;
    }

    public long getL(){
	return this.l;
    }
    
    public void setL(long l){
	this.l = l;
    }

    public int getI(){
	return this.i;
    }
    
    public void setI(int i){
	this.i = i;
    }

    public short getS(){
	return this.s;
    }
    
    public void setS(short s){
	this.s = s;
    }

    public char getC(){
	return this.c;
    }
    
    public void setC(char c){
	this.c = c;
    }

    public boolean getB(){
	return this.b;
    }
    
    public void setB(boolean b){
	this.b = b;
    }

    public byte getBy(){
	return this.by;
    }
    
    public void setBy(byte by){
	this.by = by;
    }
}